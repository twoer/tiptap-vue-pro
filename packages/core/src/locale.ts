export type LocaleKey = keyof typeof ZH_CN_LOCALE_MESSAGES
export type LocaleMessages = Record<LocaleKey, string>
export type LocaleMessageOverrides = Partial<Record<LocaleKey, string>>

export interface LocaleOptions {
  locale?: LocaleCode
  messages?: LocaleMessageOverrides
}

export type LocaleProp = LocaleCode | LocaleOptions

export interface ResolvedLocale {
  locale: LocaleCode
  messages: LocaleMessages
  t: LocaleTranslate
}

export type LocaleTranslate = (
  key: LocaleKey,
  paramsOrFallback?: Record<string, string | number> | string,
  fallback?: string,
) => string

