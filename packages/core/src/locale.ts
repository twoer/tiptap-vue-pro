const LOCALE_MESSAGES: Record<LocaleCode, LocaleMessages> = {
  'zh-CN': ZH_CN_LOCALE_MESSAGES,
  'en-US': EN_US_LOCALE_MESSAGES,
}

function normalizeLocaleProp(input?: LocaleProp): LocaleOptions {
  if (!input) return { locale: DEFAULT_LOCALE }
  if (typeof input === 'string') return { locale: input }
  return input
}

function interpolate(message: string, params?: Record<string, string | number>) {
  if (!params) return message
  return message.replace(/\{(\w+)\}/g, (match, key) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : match,
  )
}

export function resolveLocale(input?: LocaleProp): ResolvedLocale {
  const normalized = normalizeLocaleProp(input)
  const locale = normalized.locale ?? DEFAULT_LOCALE
  const messages: LocaleMessages = {
    ...LOCALE_MESSAGES[locale],
    ...(normalized.messages ?? {}),
  }

  return {
    locale,
    messages,
    t(key, paramsOrFallback, fallback) {
      const fallbackText = typeof paramsOrFallback === 'string' ? paramsOrFallback : fallback
      const params = typeof paramsOrFallback === 'object' ? paramsOrFallback : undefined
      return interpolate(messages[key] ?? fallbackText ?? key, params)
    },
  }
}
